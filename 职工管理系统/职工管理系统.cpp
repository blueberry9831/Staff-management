#include"workerManager.h"

int main() {

	/*Worker* worker = NULL;
	worker = new Manager(1, "张三", 1);
	worker->ShowInfo();*/

	//实例化管理者对象
	WorkerManager wm;

	int choice = 0;
	while (true)
	{
		//菜单
		wm.Show_Menu();

		cout << "请输入您的选择：" << endl;
		cin >> choice;//接受用户的选项

		switch (choice)
		{
		case 1:    //增加职工
			wm.Add_Emp();
			break;

		case 2:    //显示职工
			wm.Show_Emp();
			break;

		case 3:    //删除职工
			wm.Delete_Emp();
			break;

		case 4:    //修改职工
			wm.Mod_Emp();
			break;

		case 5:    //查找职工
			wm.Find_Emp();
			break;

		case 6:    //排序职工
			wm.Sort_Emp();
			break;

		case 7:    //清空文档
			wm.Clean_Emp();
			break;

		case 0:    //退出系统
			wm.ExitSystem();
			break;

		default:
			system("cls");
			break;
		}

	}

	system("pause");
	return 0;
}